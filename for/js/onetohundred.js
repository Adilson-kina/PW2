// loop para comparação para iterar para cada um dos valores
for (let i = 0; i <= 100; i++) {
  // comparação para saber se o valor é par
  if (i % 2 == 0) {
    // output caso o valor seja par
    console.log(`Par: %d`, i)
  }
} 
// loop para comparação para iterar para cada um dos valores
for (let i = 0; i <= 100; i++) {
  // comparação para saber se o valor é impar
  if (i % 2 == 1) {
    // output caso o valor seja impar
    console.log(`Impar: %d`, i)
  }
}
