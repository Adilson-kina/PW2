// Comparação para output de numeros pares e impares
for (let i = 0; i <= 100; i++) {
  if (i % 2 == 0) {
    console.log(`Par: %d`, i)
  }
} 
for (let i = 0; i <= 100; i++) {
  if (i % 2 == 1) {
    console.log(`Impar: %d`, i)
  }
}
